#!/bin/bash

# ########################################################################################
# 日期遍历
# ########################################################################################

start_date=`date -d "2019-01-27" +%Y-%m-%d`
end_date=`date -d "2019-02-09" +%Y-%m-%d`

while [[ $start_date < $end_date ]] || [[ $start_date = $end_date ]]
do
    echo "######## $start_date #########"

    start_date=`date -d "+1 day $start_date" +%Y-%m-%d`
done